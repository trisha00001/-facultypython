""""Написать не менее 10 базовых тестов на основные операции с базовыми типами данных
(строки, числа, списки, словари, кортежи, множества"""

# длина строки
def dlina(s):
    return len(s)

# рвыенство длин двух списков
def eqvali(a, b):
    return len(a) == len(b)

# перевод чсла в строчку
def chislo(a):
    return str(a)

# последний элемент в кортеже
def ruplee(a):
    return a[-1]

# значение по ключу s в словаре
def dictionary(a, s):
    return a.get(s)

# объединеие двух множеств
def mnoge(a, b):
    return a&b



